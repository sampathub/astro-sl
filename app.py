# --- 3. සිතියම් ඇඳීම (Optimized Charts) ---
def draw_astro_chart(pos_map, lag_idx):
    img = Image.new('RGB', (450, 450), (255, 255, 255))
    d = ImageDraw.Draw(img)
    for i in range(5):
        d.line([(i*112, 0), (i*112, 450)], "black", 2)
        d.line([(0, i*112), (450, i*112)], "black", 2)
    grid = {11:(0,0), 0:(112,0), 1:(224,0), 2:(336,0), 10:(0,112), 3:(336,112), 9:(0,224), 4:(336,224), 8:(0,336), 7:(112,336), 6:(224,336), 5:(336,336)}
    for r, (x, y) in grid.items():
        if r == lag_idx: d.text((x+50, y+10), "L", (255, 0, 0))
        for i, p in enumerate(pos_map.get(r, [])):
            txt = {"රවි":"Sun","සඳු":"Moon","කුජ":"Mars","බුධ":"Merc","ගුරු":"Jup","සිකුරු":"Ven","ශනි":"Sat","රාහු":"Rahu"}.get(p,p)
            d.text((x+20, y+35+(i*18)), txt, "black")
    return img

